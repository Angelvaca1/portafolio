# Inicializamos la Variable count con el Valor -10
count = -10
# Definimos el Valor Máximo de num
num = 10

# Mientras count sea Menor o Igual que num, Ejecutaremos el Bloque de Código Dentro del Bucle
while count <= num:
    # Imprimimos el Valor Actual de count
    print(count)
    # Incrementamos el Valor de count en 1 en Cada Iteración
    count = count + 1
