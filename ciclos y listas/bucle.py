# Inicializamos una Variable 'count' Para llevar la Cuenta de lo que Pase en el Bucle.
count = 0

# Mientras que 'count' sea Menor que 5, se Ejecutará el Bucle.
while count < 5:
    # Mandamos el Comando Print indicando que Python es genial.
    print("¡Python es genial!")
    # Incrementamos el Valor de 'count' para Controlar el Número de Veces que se Ejecuta el Bucle.
    count = count + 1
