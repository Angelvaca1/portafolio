# Inicializamos la Variable 'count' Para llevar la Cuenta del Número de Ovejas.
count = 1
# Mientras que 'count' sea Menor que 21, se Ejecutará el Bucle.
while count < 21:
    # Imprimimos el Número de Ovejas Seguido del Texto "Ovejas".
    print(str(count) + " ovejas")
    # Incrementamos el Valor de 'count' en 1, Cada Iteración Para Contar Cada Oveja.
    count += 1
